// ---------------------------------------------------------------------------
// Low-level helpers
// ---------------------------------------------------------------------------
static esp_err_t ssd1306_cmd(uint8_t cmd)
{
    uint8_t buf[2] = {CTRL_CMD, cmd};
    return i2c_master_transmit(s_dev, buf, 2, pdMS_TO_TICKS(50));
}

static esp_err_t ssd1306_cmd2(uint8_t cmd, uint8_t arg)
{
    uint8_t buf[3] = {CTRL_CMD, cmd, arg};
    return i2c_master_transmit(s_dev, buf, 3, pdMS_TO_TICKS(50));
}

static void set_cursor_raw(uint8_t page, uint8_t col)
{
    uint8_t buf[4] = {
        CTRL_CMD,
        0xB0 | (page & 0x07),
        0x00 | (col & 0x0F),
        0x10 | ((col >> 4) & 0x0F),
    };
    if (i2c_master_transmit(s_dev, buf, 4, pdMS_TO_TICKS(50)) != ESP_OK) {
        i2c_recover();
    }
}

// ---------------------------------------------------------------------------
// Public API
// ---------------------------------------------------------------------------
void lcd_init(i2c_master_bus_handle_t bus_handle, uint8_t i2c_addr)
{
    s_bus = bus_handle;
    i2c_device_config_t dev_cfg = {
        .dev_addr_length = I2C_ADDR_BIT_LEN_7,
        .device_address  = i2c_addr,
        .scl_speed_hz    = 400000,
    };
    ESP_ERROR_CHECK(i2c_master_bus_add_device(bus_handle, &dev_cfg, &s_dev));

    vTaskDelay(pdMS_TO_TICKS(10));

    // SSD1306 initialisation sequence
    ssd1306_cmd(0xAE);              // display off
    ssd1306_cmd2(0xD5, 0x80);       // clock divide ratio / oscillator freq
    ssd1306_cmd2(0xA8, 0x3F);       // multiplex ratio = 64
    ssd1306_cmd2(0xD3, 0x00);       // display offset = 0
    ssd1306_cmd(0x40);              // display start line = 0
    ssd1306_cmd2(0x8D, 0x14);       // charge pump on
    ssd1306_cmd2(0x20, 0x02);       // page addressing mode
    ssd1306_cmd(0xA1);              // segment re-map (col 127 → SEG0)
    ssd1306_cmd(0xC8);              // COM scan direction (remapped)
    ssd1306_cmd2(0xDA, 0x12);       // COM pins hardware config
    ssd1306_cmd2(0x81, 0xCF);       // contrast
    ssd1306_cmd2(0xD9, 0xF1);       // pre-charge period
    ssd1306_cmd2(0xDB, 0x40);       // VCOMH deselect level
    ssd1306_cmd(0xA4);              // output follows RAM
    ssd1306_cmd(0xA6);              // normal display (not inverted)
    ssd1306_cmd(0xAF);              // display on

    lcd_clear();
    ESP_LOGI(TAG, "SSD1306 OLED initialised at 0x%02X", i2c_addr);
}

void lcd_clear(void)
{
    // Write zeros to all 8 pages × 128 columns
    uint8_t buf[129];
    buf[0] = CTRL_DATA;
    memset(buf + 1, 0x00, 128);

    for (uint8_t page = 0; page < 8; page++) {
        set_cursor_raw(page, 0);
        i2c_master_transmit(s_dev, buf, 129, pdMS_TO_TICKS(100));
    }
}

void lcd_set_cursor(uint8_t row, uint8_t col)
{
    set_cursor_raw(row & 0x07, (uint8_t)(col * CHAR_WIDTH));
}

void lcd_write_string(const char *str)
{
    // Batch entire string into one I2C transaction: CTRL_DATA + 6 bytes per char.
    // 21 chars max per row = 127 bytes total — well within I2C buffer limits.
    uint8_t buf[1 + CHARS_PER_ROW * 6];
    buf[0] = CTRL_DATA;
    uint8_t n = 0;
    while (*str && n < CHARS_PER_ROW) {
        char c = *str++;
        if (c < 0x20 || c > 0x7E) c = ' ';
        const uint8_t *glyph = k_font[(uint8_t)(c - 0x20)];
        uint8_t *dst = &buf[1 + n * 6];
        dst[0] = glyph[0]; dst[1] = glyph[1]; dst[2] = glyph[2];
        dst[3] = glyph[3]; dst[4] = glyph[4]; dst[5] = 0x00;
        n++;
    }
    if (n > 0) {
        if (i2c_master_transmit(s_dev, buf, 1 + n * 6, pdMS_TO_TICKS(200)) != ESP_OK) {
            i2c_recover();
        }
    }
}
