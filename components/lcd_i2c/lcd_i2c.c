/*
 * lcd_i2c.c — SSD1306 OLED driver with HD44780-compatible text API.
 *
 * Renders text using a 5x8 font at 6 pixels per character (5 + 1 spacer).
 * 128-pixel wide display = 21 chars per row. 8 pages (rows) available.
 * We use page 0 = row 0, page 1 = row 1, matching the character LCD API.
 */

#include "lcd_i2c.h"
#include "esp_log.h"
#include "freertos/FreeRTOS.h"
#include "freertos/task.h"
#include "string.h"

static const char *TAG = "lcd_i2c";

#define SSD1306_WIDTH       128
#define CHAR_WIDTH          6       // 5 font pixels + 1 spacer
#define CHARS_PER_ROW       (SSD1306_WIDTH / CHAR_WIDTH)   // 21

// SSD1306 I2C control bytes
#define CTRL_CMD            0x00
#define CTRL_DATA           0x40

static i2c_master_bus_handle_t s_bus = NULL;
static i2c_master_dev_handle_t s_dev = NULL;

static void i2c_recover(void)
{
    if (s_bus) {
        i2c_master_bus_reset(s_bus);
    }
}

